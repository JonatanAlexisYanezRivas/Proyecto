    private void btnProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProActionPerformed
        // TODO add your handling code here:
        int n = Integer.parseInt(JOptionPane.showInputDialog("Numero de materias a promediar"));
        double calificacion = 0.0;
        double promedio = 0.0;
        for (int i = 0; i <n; i++) {
            calificacion = Double.parseDouble(JOptionPane.showInputDialog(null,"ingresa la calificacion"));
            promedio +=calificacion;
        }
        double promediofinal = promedio/n;
        String pro = String.valueOf(promediofinal);
        txtPromedio.setText(pro);
    }//GEN-LAST:event_btnProActionPerformed

