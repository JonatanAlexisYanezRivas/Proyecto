    private void Cargar(){
        Files conexion = new Files();
        if(conexion.VerificaArch()){
            if (conexion.Leer()){
                Datos = conexion.getDatos();
                CargarRegistrosTabla();
            }else{
            JOptionPane.showMessageDialog(this,
                        "No se puede leer la informacion",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
        }
    }else{
            JOptionPane.showMessageDialog(this,"No existe el archivo, ingrese información",
                    "no existe", 
                    JOptionPane.INFORMATION_MESSAGE);   
        }
    }
    
     private void CargarRegistrosTabla(){
         //aqui puede haber error
        Object[] filatabla = new Object[4];
        for(Atributos dato : Datos){
            filatabla[0]=dato.getMatricula();
            filatabla[1]=dato.getNombre();
            filatabla[2]=dato.getCorreo();
            filatabla[3]=dato.getPromedio();
            modelotabla.addRow(filatabla);
        }
        tbtDatos.setModel(modelotabla);
    }
     
     private void TitulosTabla(){
        modelotabla.addColumn("Matricula");
        modelotabla.addColumn("Nombre");
        modelotabla.addColumn("Correo");
        modelotabla.addColumn("Promedio");
        tbtDatos.setModel(modelotabla);
        
    }
     
     
