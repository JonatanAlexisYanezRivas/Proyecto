    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
           Files conexion = new Files();
           Atributos dato = new Atributos();
           dato.setMatricula(txtMatricula.getText().toString());
           dato.setNombre(txtNombre.getText().toString());
           dato.setCorreo(txtCorreo.getText().toString());
           dato.setPromedio(txtPromedio.getText().toString());
           
           conexion.Agregar(dato);
           Datos = conexion.getDatos();
           if (conexion.Grabar(Datos, 0)){
               modelotabla = (DefaultTableModel) tbtDatos.getModel();
               modelotabla.getDataVector().removeAllElements();
               checar.setText("✔");
               Cargar();
           }else{
               checar.setText("X");
           }
    }//GEN-LAST:event_btnGrabarActionPerformed

    private void tbtDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbtDatosMouseClicked
        // TODO add your handling code here:
        
        int nfilas = tbtDatos.getSelectedRowCount();
        int fila = 0;
        if(nfilas == 1){
            fila = tbtDatos.getSelectedRow();
            posicion = fila;
            txtMatricula.setText(tbtDatos.getValueAt(fila,0).toString());
            txtNombre.setText(tbtDatos.getValueAt(fila,1).toString());
            txtCorreo.setText(tbtDatos.getValueAt(fila,2).toString());
            txtPromedio.setText(tbtDatos.getValueAt(fila,3).toString());
        }
    }//GEN-LAST:event_tbtDatosMouseClicked

